// Load package details on page load
document.addEventListener("DOMContentLoaded", () => {
  const urlParams = new URLSearchParams(window.location.search);
  const packageName = urlParams.get("package");

  if (!packageName || !packageData[packageName]) {
    alert("Package not found!");
    window.location.href = "packages.html";
    return;
  }

  const pkg = packageData[packageName];

  // Populate header
  document.getElementById("packageImage").src = pkg.image;
  document.getElementById("packageName").textContent = packageName;
  document.getElementById("packageDuration").textContent = pkg.duration;
  document.getElementById("packagePrice").textContent = pkg.price;

  // Populate sidebar
  document.getElementById("sidePrice").textContent = pkg.price;
  document.getElementById("sideDuration").textContent = pkg.duration;

  // Populate overview
  document.getElementById("packageOverview").textContent = pkg.overview;

  // Populate highlights
  const highlightsList = document.getElementById("packageHighlights");
  pkg.highlights.forEach(highlight => {
    const li = document.createElement("li");
    li.textContent = highlight;
    highlightsList.appendChild(li);
  });

  // Populate itinerary
  const itineraryDiv = document.getElementById("packageItinerary");
  pkg.itinerary.forEach(day => {
    const dayDiv = document.createElement("div");
    dayDiv.className = "itinerary-day";
    dayDiv.innerHTML = `
      <h4>${day.day}: ${day.title}</h4>
      <p>${day.description}</p>
    `;
    itineraryDiv.appendChild(dayDiv);
  });

  // Populate inclusions
  const inclusionsList = document.getElementById("packageInclusions");
  pkg.inclusions.forEach(item => {
    const li = document.createElement("li");
    li.textContent = item;
    inclusionsList.appendChild(li);
  });

  // Populate exclusions
  const exclusionsList = document.getElementById("packageExclusions");
  pkg.exclusions.forEach(item => {
    const li = document.createElement("li");
    li.textContent = item;
    exclusionsList.appendChild(li);
  });

  // Book Now button
  const bookNowBtn = document.getElementById("bookNowBtn");
  const modal = document.getElementById("bookingModal");
  const closeModal = document.querySelector(".close-modal");
  const modalTitle = document.getElementById("modalTitle");
  const modalDuration = document.getElementById("modalDuration");
  const modalPrice = document.getElementById("modalPrice");

  bookNowBtn.addEventListener("click", () => {
    const isLoggedIn = localStorage.getItem("isLoggedIn");
    
    if (isLoggedIn !== "true") {
      window.location.href = "login.html";
      return;
    }

    modalTitle.textContent = packageName;
    modalDuration.textContent = pkg.duration;
    modalPrice.textContent = pkg.price;
    modal.style.display = "flex";
    document.body.style.overflow = "hidden";
  });

  closeModal.addEventListener("click", () => {
    modal.style.display = "none";
    document.body.style.overflow = "auto";
  });

  window.addEventListener("click", (e) => {
    if (e.target === modal) {
      modal.style.display = "none";
      document.body.style.overflow = "auto";
    }
  });

  // Booking form submission
  const bookingForm = document.getElementById("bookingForm");
  const customerNameInput = document.getElementById("customerName");
  const travelDateInput = document.getElementById("travelDate");
  const travelersInput = document.getElementById("travelers");

  // Set minimum date to today
  const today = new Date().toISOString().split("T")[0];
  travelDateInput.min = today;

  bookingForm.addEventListener("submit", (e) => {
    e.preventDefault();

    const customerName = customerNameInput.value.trim();
    const travelDate = travelDateInput.value;
    const travelers = parseInt(travelersInput.value, 10);

    if (!customerName || !travelDate || !travelers) {
      alert("Please fill all fields.");
      return;
    }

    if (travelDate < today) {
      alert("Travel date cannot be in the past.");
      return;
    }

    if (travelers < 2) {
      alert("Minimum 2 travelers required for this package.");
      return;
    }

    const booking = {
      type: "package",
      user: localStorage.getItem("username"),
      packageName: packageName,
      duration: pkg.duration,
      price: pkg.price,
      date: travelDate,
      travelers: travelers,
      customerName: customerName,
      state: "N/A", // Packages don't have specific states
      plan: "Standard Package", // Default plan for packages
    };

    let bookings = JSON.parse(localStorage.getItem("bookings")) || [];
    bookings.push(booking);
    localStorage.setItem("bookings", JSON.stringify(bookings));

    alert("Booking confirmed successfully!");
    modal.style.display = "none";
    document.body.style.overflow = "auto";
    bookingForm.reset();
  });
});
